def main():
    print("This Program will add two numbers")
    num1 = str(input("Enter the first number: "))
    num1 = int(num1)
    num2 = str(input("Enter the second number: "))
    num2 = int(num2)
    sum = num1 + num2
    print("The sum of two numbers is: ", sum)

if __name__ == "__main__":
    main()
