// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
var person = "Canon";
console.log(person.toLowerCase()); //All letter in small Alphabets
console.log(person.toUpperCase()); //All letter in large Alphabets
console.log(person.charAt(0).toUpperCase() + person.slice(1).toLowerCase()); //First letter in big alphabet.
